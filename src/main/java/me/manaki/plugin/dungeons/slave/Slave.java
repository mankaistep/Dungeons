package me.manaki.plugin.dungeons.slave;

public class Slave {
	
	private String name;
	private int color;
	
	public Slave(String name, int color) {
		this.name = name;
		this.color = color;
	}
	
	public String getName() {
		return this.name;
	}
	
	public int getColor() {
		return this.color;
	}
	
}
